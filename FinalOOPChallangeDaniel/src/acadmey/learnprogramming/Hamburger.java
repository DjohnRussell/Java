package acadmey.learnprogramming;

public class Hamburger {
    private String bun;
    private double basePrice;

    public Hamburger() {
        this.bun = bun;
        this.basePrice = 6.99;
    }

    public String getBun() {
        return bun;
    }

    public double getBasePrice() {
        return basePrice;
    }
}
